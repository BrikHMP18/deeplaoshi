// Helper function to get word count for a specific letter
export const getWordCount = (letter) => {
  return greVocabulary[letter]?.length || 0;
};

// Helper function to get total word count
export const getTotalWordCount = () => {
  return Object.values(greVocabulary).reduce((total, words) => total + words.length, 0);
};
